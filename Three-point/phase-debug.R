  M11 <- 1:4
  m1_l <- cbind(matrix(rep(M11,2),nrow=2,byrow =T),matrix(M11[combn(4,2)],2))
  m1_l1 <- sort_g(m1_l)
  m1_g <- m1_l1[1,]*10+m1_l1[2,]
  
  M22 <- 1:4
  m2_l <- cbind(matrix(rep(M22,2),nrow=2,byrow =T),matrix(M22[combn(4,2)],2))
  m2_l1 <- sort_g(m2_l)
  m2_g <- m2_l1[1,]*10+m2_l1[2,]
  
  M33 <- 1:4
  m3_l <- cbind(matrix(rep(M33,2),nrow=2,byrow =T),matrix(M33[combn(4,2)],2))
  m3_l1 <- sort_g(m3_l)
  m3_g <- m3_l1[1,]*10+m3_l1[2,]
  
  nm <- array(0,dim=c(length(m1_g),length(m2_g),length(m3_g)))
  nm1 <- array(0,dim=c(length(m1_g),length(m2_g),length(m3_g)))
  for(i in 1:length(m1_g)){
    for(j in 1:length(m2_g)){
      for(k in 1:length(m3_g)){
        
        nm[i,j,k] <- m1_g[i]*10000+m2_g[j]*100+m3_g[k]
        nm1[i,j,k] <- (m1_g[i]%/%10*100+m2_g[j]%/%10*10+m3_g[k]%/%10)*1000+(m1_g[i]%%10*100+m2_g[j]%%10*10+m3_g[k]%%10)
      }
    }
  }
  
  m_nm <- melt_m(nm)
  m_nm1<- melt_m(nm1)
  return(list(m_nm=m_nm,m_nm1=m_nm1))
}

gf <- function(i1,i2,i3){
  
  rc <- c()
  for(i in i1){
    for(j in i2){
      for(k in i3){
        
        rc <- c(rc,i*100+j*10+k)
        
      }
    }
  }
  rc
}

poschange <- function(f1,f2){
  
  
  tmp11 <- sort_g(rbind(f1%/%100,f2%/%100))
  tmp22 <- sort_g(rbind(f1%%100%/%10,f2%%100%/%10))
  tmp33 <- sort_g(rbind(f1%%100%%10,f2%%100%%10))
  ct1 <- tmp11[1,]*100+tmp22[1,]*10+tmp33[1,]
  ct2 <- tmp11[2,]*100+tmp22[2,]*10+tmp33[2,]
  ct <- ct1*1000+ct2
  ct
}

